package com.mjedli.paybuy.invoice.model;

/**
 * @author mjedli
 *
 */
public class Result {
	
	private String resultDay = "";
	private String resultMonth = "";
	private String resultYear = "";
	
	public String getResultDay() {
		return resultDay;
	}
	public void setResultDay(String resultDay) {
		this.resultDay = resultDay;
	}
	public String getResultMonth() {
		return resultMonth;
	}
	public void setResultMonth(String resultMonth) {
		this.resultMonth = resultMonth;
	}
	public String getResultYear() {
		return resultYear;
	}
	public void setResultYear(String resultYear) {
		this.resultYear = resultYear;
	}
	
}
